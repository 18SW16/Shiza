class Scanner{
public static void main(String[]args){
Scanner sc=new Scanner(System.in);
String str=sc.nextline();
String spilt[]=str.spilt(" ,");
      //Student 1 data
      
System.out.println("Student 1:");
System.out.println("Name: "+spilt[0]);
System.out.println("Age: "+spilt[1]);
System.out.println("Program: "+spilt[2]);

     //Student 2 data
 
System.out.println("\n\nStudent 2:");
System.out.println("Name: "+spilt[3]);
System.out.println("Age: "+spilt[4]);
System.out.println("Program: "+spilt[5]);
}
}
 

